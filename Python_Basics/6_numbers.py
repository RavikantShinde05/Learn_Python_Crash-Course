# OTHER OPERATIONS:

print(10 + 2)  # output> 12
print(10 - 2)  # output> 8
print(10 * 2)  # output> 20
print(10 / 2)  # output> 5.0
print(10 / 3)  # output> 3.3333
print(10 // 3)  # output> 3
print(10 % 2)  # output> 0
print(10 % 3)  # output> 1
print(10 ** 2)  # output> 100

